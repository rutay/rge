
#pragma once

#include <cmath>

namespace rge
{

// ------------------------------------------------------------------------------------------------
// Vec
// ------------------------------------------------------------------------------------------------

template<size_t Dim, typename T>
struct Vec;

// ------------------------------------------------------------------------------------------------
// Vec3
// ------------------------------------------------------------------------------------------------

template<typename T>
struct Vec<3, T>
{
	union {
		struct { float x, y, z; };
		struct { float r, g, b; };
	};
};

using Vec3 = Vec<3, float>;

// ------------------------------------------------------------------------------------------------
// Vec4
// ------------------------------------------------------------------------------------------------

template<typename T>
struct Vec<4, T>
{
	union {
		struct { float x, y, z, w; };
		struct { float r, g, b, a; };
	};
};

using Vec4 = Vec<4, float>;
}

// ------------------------------------------------------------------------------------------------
// Quaternion
// ------------------------------------------------------------------------------------------------

struct Quaternion
{
	float x, y, z, w;
};
